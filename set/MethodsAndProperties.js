/* Properties */
size; // Returns the number of elements in the set.

/* Methods */
forEach(callbackFn, thisArg); // Executes a function for each value in the set.
entries(); // Returns an iterator of the set's [value, value] pairs.
keys(); // Alias for values().
values(); // Returns an iterator of the set's values.
add(value); // Adds a new element to the set.
delete value; // Removes an element from the set.
has(value); // Checks if an element exists in the set.
clear(); // Removes all elements from the set.
